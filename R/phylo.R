        cost <- cost - diag(l)
    }
    tree$edge.length=NULL
            swap = 0
            iter = TRUE
            pscore <- fit.sankoff(tree,dat,cost)
            while (iter) {
                res <- sankoff.nni(tree,dat,cost,...)
                tree <- res$tree
                cat("optimize topology: ", pscore , "-->", res$pscore, 
