        optRate = FALSE
    }
    trace <- control$trace
    Q = object$Q
    bf = object$bf
    eig = object$eig
    inv = object$inv
    k = object$k
    shape = object$shape
    w = object$w
    g = object$g
    dat = object$data
    ll0 <- object$logLik
    INV <- object$INV
    ll.0 <- object$ll.0
    rate <- object$rate
    ll = ll0
    ll1 = ll0
    opti = TRUE
    if (optEdge) {
        object <- optimEdge(object, control = pml.control(eps = 0.001, maxit = 5, trace))
        ll <- object$logLik
        tree <- object$tree
    }
    rounds = 1
    df = 0
    while (opti) {
        if (optBf) {
            res = optimBf(tree, dat, bf = bf, inv = inv, Q = Q, 
                w=w, g=g, INV=INV, rate=rate, k=k, llMix=llMix)
            bf = res[[1]]
            eig = edQt(Q = Q, bf = bf)
            if(inv>0) ll.0 <- INV %*% (bf * inv)
            if(wMix>0) ll.0 <- ll.0 + llMix
            cat("optimize base frequencies: ", ll, "-->", res[[2]], 
                "\n")
            ll = res[[2]]
        }
        if (optQ) {
            res = optimQ(tree, dat, Q=Q, bf=bf, w=w, g=g, 
                inv=inv, INV=INV, ll.0=ll.0, rate=rate, k=k)
            Q = res[[1]]
            eig = edQt(Q = Q, bf = bf)
            cat("optimize rate matrix: ", ll, "-->", res[[2]], "\n")
            ll = res[[2]]
        }
        if (optInv) {
            res = optimInv(tree, dat, inv = inv, INV = INV, Q = Q, 
                bf = bf, eig = eig, k = k, shape = shape, rate = rate)
            inv = res[[1]]
            w = rep(1/k, k)
            g = discrete.gamma(shape, k)
            w = (1 - inv) * w
            if (wMix > 0) w <- (1 - wMix) * w 
            g = g/(1 - inv)
            g <- g * rate
            ll.0 = INV %*% (bf * inv)
            if(wMix>0) ll.0 <- ll.0 + llMix
            cat("optimize invariant sites: ", ll, "-->", res[[2]], 
                "\n")
            ll = res[[2]]
        }
        if (optGamma) {
            res = optimGamma(tree, dat, shape = shape, k = k, 
                inv = inv, INV = INV, Q = Q, bf = bf, eig = eig, 
                ll.0 = ll.0, rate = rate)
            shape = res[[1]]
            w = rep(1/k, k)
            g = discrete.gamma(shape, k)
            if (inv > 0) {
                w = (1 - inv) * w
                g = g/(1 - inv)
            }
            if (wMix > 0) w <- (1 - wMix) * w 
            g <- g * rate
            cat("optimize shape parameter: ", ll, "-->", res[[2]], 
                "\n")
            ll = res[[2]]
        }
        if (optRate) {
            res = optimRate(tree, dat, rate = rate, inv = inv, 
                INV = INV, Q = Q, bf = bf, eig = eig, k = k, 
                shape = shape, w = w, ll.0 = ll.0)
            rate = res[[1]]
            g = discrete.gamma(shape, k)
            w = rep(1/k, k) 
            if (inv > 0) {
                w = (1 - inv) * w
                g = g/(1 - inv)
            }
            if (wMix > 0) w <- (1 - wMix) * w 
            g <- g * rate
            cat("optimize rate: ", ll, "-->", res[[2]], "\n")
            ll = res[[2]]
        }
        if (optEdge) {
           object <-update.pml(object, tree=tree, dat=dat, Q=Q, bf=bf,
                 inv=inv, shape=shape, k=k, rate=rate, wMix=wMix, llMix=llMix)
            object <- optimEdge(object, control = list(eps = 0.001, 
                maxit = 5))
            ll <- object$logLik
            tree <- object$tree
        }
        if (optNni) {
            swap = 0
            iter = 1
            while (iter < 4) {
                tree <- object$tree
                object <- pml.nni(object)
                tree <- object$tree
                cat("optimize topology: ", ll, "-->", object$logLik, 
                  "\n")
                ll = object$logLik
                swap = swap + object$swap
                iter = iter + 1
                if (object$swap == 0) {
                  iter = 4
                  optNni = FALSE
                }
            }
            cat(swap, "\n")
            if (swap > 0) 
                rounds = 1
            if (swap == 0) 
                optNni = FALSE
        }
        rounds = rounds + 1
        if (rounds > control$maxit) 
            opti = FALSE
        if (abs(ll1 - ll) < control$eps) 
            opti = FALSE
        ll1 = ll
    }
    df = (optEdge) * length(tree$edge.length) + optGamma * (k - 
        1) + optInv * (inv > 0) + optBf * (length(unique(bf)) - 
        1) + optQ * (length(unique(Q)) - 1)
      object <-update.pml(object, tree=tree, dat=dat, Q=Q, bf=bf,
         inv=inv, shape=shape, k=k, rate=rate, wMix=wMix, llMix=llMix)        
    object$df = df
    object
}



optimEdge <- function (fit, control = list(eps = 1e-08, maxit = 20, trace = 0), 
    ...) 
{
    if (class(fit)[1] != "pml") 
        stop("data must be of class pml")
    tree = fit$tree
    if (is.null(attr(tree, "order")) || attr(tree, "order") == 
        "cladewise") 
        tree <- reorderPruning(tree)
    fit$tree <- tree
    el <- tree$edge.length
    tree$edge.length[el < 0] <- 1e-08
    dat <- NULL    
    rate <- fit$rate 
#    browser()
    old.ll <- pml5(fit)
    
#    contrast <- attr(data, "contrast")
    eig <- fit$eig
    w <- fit$w
    g <- fit$g
    bf <- fit$bf
    weight <- attr(fit$data, "weight")
    ll.0 <- fit$ll.0
    eps = 1
    iter = 0
    child = tree$edge[, 2]
    parent = tree$edge[, 1]
    nTips = min(parent) - 1
    n = length(tree$edge.length)
    k = length(w)
    data <- new2old.phyDat(fit$data)
#    lt = length(tree$tip)
#    for(i in 1:k)dat[i, 1:lt]=data
    
    
    child.dat = vector("list", k)
    while (eps > control$eps && iter < control$maxit) {
        for (j in n:1) {
#        browser()
            if (child[j] > nTips) 
               child.dat = dat[, child[j]]
            else{
#                for(i in 1:k)child.dat[[i]] = contrast[data[[ child[j] ]], , drop=FALSE]
                child.dat[] = data[ child[j] ]
                }
            parent.dat = dat[, parent[j]]
            old.el = tree$edge.length[j]
            newEL <- fs(old.el, eig, parent.dat, child.dat, weight, 
                g = g, w = w, bf = bf, ll.0 = ll.0)
            el[j] = newEL[[1]]
            dat[, parent[j]] = newEL[[2]]
            if (child[j] > nTips) {
                dat[, child[j]] = newEL[[3]]
            }
        }
        tree$edge.length = el
        iter = iter + 1
        fit$tree = tree
        dat <- NULL
        newll <- pml5(fit)
        eps = newll - old.ll
        old.ll = newll
    }
    cat(fit$logLik, " -> ", newll, "\n")
    fit$logLik = newll
    fit
}


