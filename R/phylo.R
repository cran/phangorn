        PACKAGE = "phangorn")
    res <- data.frame(distance = result, edges = -fhm(result)/2^(n - 
        2), index = 0:(ns - 1))
    attr(result, "Labels") <- Labels
    res
}


h4st = function(obj, levels=c('a','c','g','t')){
#	if(is.matrix(obj)) obj = as.data.frame(t(obj))

#	DNA = as.data.frame(obj)
	DNA = t(as.character(obj))

	n = dim(DNA)[1]
	p = dim(DNA)[2]

	if(p>11) stop("4-state Hadamard conjugation works only efficient for n < 12")

	DNAX = matrix(0,n,p)
	DNAY = matrix(0,n,p)

	DNAX[DNA==levels[1]]=0
	DNAX[DNA==levels[2]]=1
	DNAX[DNA==levels[3]]=1
	DNAX[DNA==levels[4]]=0

	DNAY[DNA==levels[1]]=0
	DNAY[DNA==levels[2]]=1
	DNAY[DNA==levels[3]]=0
	DNAY[DNA==levels[4]]=1

	DNAY = DNAY - DNAY[,p]
	DNAX = DNAX - DNAX[,p]

	DNAY = abs(DNAY[,-p])
	DNAX = abs(DNAX[,-p])
	dy = DNAY %*% (2^(0:(p-2))) 
	dx = DNAX %*% (2^(0:(p-2))) 

	INDEX =  dx + 2^(p-1) * dy
	blub = table(INDEX)
	index = as.numeric(rownames(blub)) + 1
	sv = numeric(4^(p-1))
	sv[index] = blub
	qv = matrix(seq2split(sv),2^(p-1),2^(p-1))
	sv = matrix(sv,2^(p-1),2^(p-1))

	q = cbind(transversion = qv[-1,1], transition.1 = diag(qv)[-1], transition.2 = qv[1,-1])
	result = list(q = q, qv = qv, sv=sv, n=sum(sv), names=names(obj))
	result
}


h2st = function (obj, levels = c("r", "y")) 
{
    if (is.matrix(obj)) 
        obj = as.data.frame(t(obj))
    #obj = as.data.frame(obj)
    if(class(obj)=="phyDat")  obj = as.data.frame(t(as.character(obj)))
    n = dim(obj)[1]
    p = dim(obj)[2]
    if (p > 23) 
        stop("Hadamard conjugation works only efficient for n < 24")
    DNAX = matrix(0, n, p)
    DNAX[obj == levels[1]] = 0
    DNAX[obj == levels[2]] = 1
    DNAX = DNAX - DNAX[, p]
    DNAX = abs(DNAX[, -p])
    dx = DNAX %*% (2^(0:(p - 2)))
    INDEX = dx 
    blub = table(INDEX)
    index = as.numeric(rownames(blub)) + 1
    sv = numeric(2^(p - 1))
    sv[index] = blub
    qv = seq2split(sv)
    result = data.frame(edges = qv, splits=sv, index = 0:(2^(p-1)-1))
    attr(result,"Labels") = names(obj)
    result
}


