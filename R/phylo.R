		eta = c(eta,0)
		p = exp(eta)/sum(exp(eta))
		res = x%*%p
		res = sum(weight*log(res))  * (1 + abs(sum(p*g) - 1))
		res
	}  
	res = optim(eta, fn = fn, method = "Nelder-Mead", control=list(fnscale=-1, reltol = 1e-12),gr=NULL, x=x,g=g, weight=weight)
	p = exp(c(res$par,0))
	p = p/sum(p)
	result = list(par = p, value = res$value)
